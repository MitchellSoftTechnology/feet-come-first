	<h2>Common foot problems</h2>

		<h3>Corns</h3>

		<p>Corns are caused by intermittent pressure over a bony prominence, and are the body's defence
		against this pressure. They can be very painful and should be professionally removed. Without
		removing the initial cause of the pressure the corn will always reform.</p>

		<h3>Calluses/hard skin</h3>

		<p>Calluses/hard skin are caused by a combination of friction and pressure, and are particularly
		prevalent on heels and with loose footwear, such as flip flops. They should be professionally
		removed, and particularly so on the heel where the skin can be susceptible to cracking as it
		thickens.</p>

		<h3>Verrucae</h3>

		<p>Verrucae are often mistaken for corns or calluses, but are in fact caused by a virus entering
		through a break in the skin. They can be very painful, and it is believed that the virus responsible
		is so prevalent that each of us comes into contact with it daily. There is a range of treatment
		available but they can be difficult to clear quickly.</p>

		<h3>Athlete's Foot</h3>

		<p>Athlete's Foot is an opportunistic fungal infection that thrives in a warm, moist, and dark
		environment. It is not a hygiene issue. It usually manifests itself as red, itchy, and painful
		skin between the toes. A range of effective proprietary products is available to treat this
		condition.</p>

		<h3>Bunions</h3>

		<p>Bunions are a deformity caused by a lateral (sideways) deviation of the big toe joint towards
		the midline of the foot. There is disagreement about their causes, with some believing that
		it is a genetic condition and others blaming tightly-fitting shoes. Bunions have been found
		in people who do not wear footwear, suggesting a genetic predisposition, with perhaps
		badly-fitting footwear exacerbating the problem. Bunions can be painful, and treatment can
		range from easing the pressure upon the affected area through to surgery.</p>

